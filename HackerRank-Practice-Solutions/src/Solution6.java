import java.util.Scanner;
class Solution6 {
    
    public static void main(String[]args){
        
        Scanner si = new Scanner(System.in);
        
        int num = si.nextInt();
        
        for(int i=1;i<=10;i++){
            System.out.println(num+" x "+i+" = "+num*i);
            
        }
         
    }
}



